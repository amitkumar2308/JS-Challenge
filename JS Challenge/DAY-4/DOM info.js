//1. Introduction to DOM Manipulation


//1.1 What is the DOM?
//Definition: The Document Object Model (DOM) is a programming interface for web documents. It represents the structure of a document as a tree of objects, where each object corresponds to a part of the document, such as elements, attributes, and text nodes.
//Purpose: DOM manipulation allows JavaScript to dynamically change the content, structure, and style of a web page.


//1.2 Basic DOM Concepts
//Nodes: Elements, attributes, and text within an HTML document.
//Elements: HTML tags (e.g., <div>, <p>, <a>) that represent parts of the document.
//Attributes: Properties of HTML elements (e.g., id, class, src).