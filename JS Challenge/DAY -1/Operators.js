//Arithmetic Operators: +, -, *, /, %, **

let sum = 5 + 10; // Addition
let product = 4 * 3; // Multiplication
let power = 2 ** 3; // Exponentiation (2^3)


//Comparison Operators: ==, ===, !=, !==, >, <, >=, <=

let isEqual = 5 == "5";  // true (loose equality, converts types)
let isStrictEqual = 5 === "5";  // false (strict equality, no type conversion)


//Logical Operators: && (AND), || (OR), ! (NOT)

let result = true && false;  // false
let result2 = true || false;  // true
let result3 = !true;  // false