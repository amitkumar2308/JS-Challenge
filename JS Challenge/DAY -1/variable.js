//Variables store data values that can be used later in your code.

//var (old), let (modern), const (for constants)


//Syntax

let myName = "John";  // String
let age = 25;         // Number
const pi = 3.14;      // Constant value


//let allows you to reassign values, while const makes a value constant (cannot be changed)

//Example
let age = 20;
age = 21;  // You can change the value
console.log(age);  // Output: 21

