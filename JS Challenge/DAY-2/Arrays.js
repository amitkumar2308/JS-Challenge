//Arrays are used to store multiple values in a single variable. Each item in an array has an index, starting from 0

let fruits = ["Apple", "Banana", "Orange"];
console.log(fruits[0]);  // Output: Apple


//You can also add, remove, or modify elements in an array
let numbers = [1, 2, 3, 4];
numbers.push(5);  // Add an item at the end
numbers.pop();    // Remove the last item
numbers[1] = 10;  // Modify an item

console.log(numbers);  // Output: [1, 10, 3, 4]
